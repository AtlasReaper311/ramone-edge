import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const SOURCE = path.join(ROOT, "assets", "browser-icons");
const OUTPUT = path.join(ROOT, "src", "browser-icons.generated.js");

const ASSETS = [
  ["/favicon.ico", "favicon.ico", "image/x-icon"],
  ["/favicon-16x16.png", "favicon-16x16.png", "image/png"],
  ["/favicon-32x32.png", "favicon-32x32.png", "image/png"],
  ["/apple-touch-icon.png", "apple-touch-icon.png", "image/png"],
  ["/android-chrome-192x192.png", "android-chrome-192x192.png", "image/png"],
  ["/android-chrome-512x512.png", "android-chrome-512x512.png", "image/png"],
  ["/site.webmanifest", "site.webmanifest", "application/manifest+json; charset=utf-8"],
];

const entries = [];
for (const [route, filename, contentType] of ASSETS) {
  const source = path.join(SOURCE, filename);
  if (!fs.existsSync(source) || fs.statSync(source).size === 0) {
    throw new Error(`missing browser icon source: ${source}`);
  }
  if (filename === "site.webmanifest") {
    JSON.parse(fs.readFileSync(source, "utf8"));
  }
  entries.push(
    `  ${JSON.stringify(route)}: Object.freeze({ contentType: ${JSON.stringify(contentType)}, base64: ${JSON.stringify(fs.readFileSync(source).toString("base64"))} })`,
  );
}

const output = [
  "// Generated by scripts/embed-browser-icons.mjs from assets/browser-icons.",
  "export const BROWSER_ICONS = Object.freeze({",
  entries.join(",\n"),
  "});",
  "",
].join("\n");

fs.writeFileSync(OUTPUT, output, "utf8");
console.log(`generated ${path.relative(ROOT, OUTPUT)} from ${ASSETS.length} local assets`);
